#include<stdio.h>
#include<math.h>
int main()
{
    printf("%f\n",sqrt(4));
    printf("exp:%f\n",exp(4));
    printf("log:%f\n",log(2));
    printf("log10:%f\n",log10(4));
    printf("fabs:%f\n",fabs(4.4));
    printf("ceil:%f\n",ceil(4.5));
    printf("floor:%f\n",floor(4.9));
    printf("power:%f\n",pow(4,2));
    printf("fmod:%f\n",fmod(2.444,1.338));
    printf("sin:%f\n",sin(1));
    printf("cos:%f\n",cos(1));
    printf("tan:%f\n",tan(1));
    return 0;
}
